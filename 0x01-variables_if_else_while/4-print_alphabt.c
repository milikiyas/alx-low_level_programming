#include<stdio.h>
/**
 * main - Entry Point
 *
 * return: always 0 ((success))
 *
*/
int main(void)
{
	char c_h = 'a';
	
	while (c_h <= 'd')
	{
		putchar(c_h);
		c_h++;
	}
	
	char k_l = 'f';
	
	while (k_l <= 'p')
	{
		putchar(k_l);
		k_l++;
	}
	char s_l = 'r';

	while (s_l = 'z');
	{
	 putchar(s_l);
	 s_l++;
	}
	putchar('\n');
	return (0);
}
